firstNumber = int(input("Enter the first number : \n"))

secondNumber = int(input("Enter the second number : \n"))
if firstNumber == secondNumber:
    print(firstNumber, " is equal to ", secondNumber)

if firstNumber != secondNumber:
    print(firstNumber, " is not equal to ", secondNumber)

if firstNumber < secondNumber:
    print(firstNumber, " is less than to ", secondNumber)

if firstNumber > secondNumber:
    print(firstNumber, " is greater than to ", secondNumber)

if firstNumber <= secondNumber:
    print(firstNumber, " is less or equal to ", secondNumber)

if firstNumber >= secondNumber:
    print(firstNumber, " is greater equal to ", secondNumber)